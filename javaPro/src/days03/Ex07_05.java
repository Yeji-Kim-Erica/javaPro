public class Ex07_05 {

	public static void main(String[] args) {
		
		// 대입(할당)연산자 =
		int i = 100; // =
		
		// 복합대입연산자 +=, -=, *=, /=, %=, <<=, >>=, &=, ^=, |=
		String name = "홍길동";
		name += "이창익"; // name = name + "이창익"; 과 동일한 효과
		System.out.println(name); // 출력결과: 홍길동이창익
		
		// 이 부분도 강사님 필기 한번 더 보기
	}
	
}
